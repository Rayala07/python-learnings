#Appending data into the file

txt_data = "Data appended successfully.."

file_path = "/content/output.txt"

with open(file_path, "a") as file:
  file.write("\n" + txt_data)

# "x" exclusive creation mode , will throw error if you create and write in a file
# Good practice to write "x" mode in a try-catch block

try:
  with open(file_path, "x") as file:
    file.write(txt_data)
except FileExistsError:
  print("File already exists")

# Writing data of a collection in a file -
# Only strings are writable so ,we need to iterate over each object of the collection list[], tuple(), set{} and write them into a file

student_names = ["Lucky", "Gana", "Pushpa", "Bunny", "Arya"]

file_path_2 = "names.txt"

with open(file_path_2, "w") as file:
  for each_student in student_names:
    file.write(each_student + "\n")

