# Writing in a .csv file (comma seperated values)
import csv

userData = [["Name", "Age", "userId"],
            ["Tony", 45, "tony.stark"],
            ["Peter", 25, "peter.parker"],
            ["Dr. Strange", 42, "stephen.strange"]]

file_path_4 = "users.csv"

with open(file_path_4, "w", newline="") as file:
  writer = csv.writer(file)
  for each_row in userData:
    writer.writerow(each_row)